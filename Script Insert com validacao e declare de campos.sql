INSERT INTO [DBO].[TB_PARAMETRO_CAMPOVALIDADOR]
			([NOME_CAMPO],
			 [ORIGEM_CAMPO],
			 [TIPO_CAMPO],
			 [USUARIO_ULTIMA_ALTERACAO],
			 [DATA_ULTIMA_ALTERACAO])
		VALUES
			('FALTA_BASE_CLUSTER', @ORIGEMCAMPO, 1, @USUARIO, @DATAATUAL)