DECLARE @DATAATUAL AS DATETIME = GETDATE()
DECLARE @ORIGEMCAMPO AS INT = 40
DECLARE @USUARIO AS VARCHAR(20) = 'GOSOSIL'

IF(SELECT TOP 1 1 FROM [TB_PARAMETRO_CAMPOVALIDADOR]
   WHERE NOME_CAMPO = 'FALTA_BASE_CLUSTER' AND ORIGEM_CAMPO = @ORIGEMCAMPO) IS NULL
